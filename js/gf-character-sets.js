export const GF_SETS = Object.keys(_data).map(name => ({ name }));

/**
 * No-op: data is already bundled. Kept so call sites don't need to change.
 */
export async function loadGFSets() {}

/**
 * Compute coverage of a named GF set against the font's glyph map.
 *
 * @param {string} setName
 * @param {Object} glyphMap — {glyphName: [codepoint, ...]}
 * @returns {{ have: number, total: number, missing: number[] }}
 */
export function coverage(setName, glyphMap) {
  const setPoints = _data[setName];
  if (!setPoints) return { have: 0, total: 0, missing: [] };

  const fontCodepoints = new Set();
  for (const unicodes of Object.values(glyphMap)) {
    for (const u of unicodes) fontCodepoints.add(u);
  }

  const missing = [];
  let have = 0;
  for (const cp of setPoints) {
    if (fontCodepoints.has(cp)) {
      have++;
    } else {
      missing.push(cp);
    }
  }
  return { have, total: setPoints.length, missing };
}

/**
 * Return the codepoints for a named set.
 */
export function getSetCodepoints(setName) {
  return _data[setName] ?? [];
}
